awk -v OFS='\t' -v WIN="$WIN" '{if ($2 - WIN > 0) print $1,$2 - WIN,$3 - 2; else print $1, 0, $3 - 1}' $BG_norm_overlap.${NAME} > $BG_norm_overlap_up.${NAME}  
awk -v OFS='\t' -v WIN="$WIN" '{print $1, $2 + 2, $3 + WIN}' $BG_norm_overlap.${NAME} > $BG_norm_overlap_down.${NAME}
echo 'step 2 complete...'
##now look for maximum in up and down files
bedtools map -a $BG_norm_overlap_up.${NAME} -b $BG_norm -c 5 -o max -null 0 > $BG_norm_overlap_up.plus.${NAME}
bedtools map -a $BG_norm_overlap_down.${NAME} -b $BG_norm -c 5 -o max -null 0 > $BG_norm_overlap_down.plus.${NAME}
echo 'step 3 complete...'
##print max of flank up/down for each to the end of reformatted norm_overlap file
awk 'BEGIN {print "sRNA\tTranscript\tSlice.site\tAllenScore\tflankMax.rpm\tslice.site.rpm\tslice.site.hit.norm"}' > $outDir/${NAME}_${NAME1}.${outDir_a}_win_${WIN}_detectedSites.txt
paste $BG_norm_overlap.${NAME} $BG_norm_overlap_up.plus.${NAME} $BG_norm_overlap_down.plus.${NAME} | awk -v OFS='\t' '{if ($11 > $15) print $4,$1,$2,$5,$11,$7,$6; else print $4,$1,$2,$5,$15,$7,$6}' | sort -k1,1 -n -f -k2,2 >> $outDir/${NAME}_${NAME1}.${outDir_a}_win_${WIN}_detectedSites.txt
echo 'step 4 complete...'
#remove intermediate files
rm $BG_norm_overlap.${NAME} $BG_norm_overlap_up.${NAME} $BG_norm_overlap_down.${NAME} $BG_norm_overlap_up.plus.${NAME} $BG_norm_overlap_down.plus.${NAME}

